# program to perform the use of continue statement

# print the odd numbers from 1 to 10 using continue statement
for i in range(1,10):
  if (i%2==0):
    continue
  print(i)


# using while loop
num=0
while num<10:
  num=num+1
  if(num%2==0):
    continue
  print(num)

